def get_dict_hash(dataset: str, model_arch: str) -> dict:
    """Get hash dictionary for a given dataset and model architecture.
    
    Args:
        dataset: Dataset name.
        model_arch: Model architecture name.
        
    Returns:
        Dictionary mapping training configuration strings to checkpoint hashes.
        
    Raises:
        ValueError: If no hash dict is found for the given dataset and model_arch.
    """
    if dataset == 'MNIST' and model_arch == 'SmallFeedforwardMNIST':
        return dict_hash_MNIST_SmallFeedforward
    elif dataset == 'MNISTStiky' and model_arch == 'SmallFeedforwardMNIST':
        return dict_hash_SmallFeedforward_MNISTStiky
    elif dataset == 'imagenet' and model_arch == 'resnet50':
        return dict_hash_resent50_imagenet
    elif dataset == 'Places365' and model_arch == 'resnet50':
        return dict_hash_resent50_Places365
    elif dataset == 'imagenetvggface2' and model_arch == 'resnet50':
        return dict_hash_resent50_imagenetvggface2
    elif dataset == 'CIFAR10':
        return dict_hash_CIFAR10
    elif dataset == 'MNIST':
        return dict_hash_MNIST
    elif dataset == 'vggface2' and model_arch == 'resnet50':
        return dict_hash_resent50_vggface2
    elif dataset == 'vggface2' and model_arch == 'resnet18':
        return dict_hash_resent18_vggface2
    elif dataset == 'vggface2' and model_arch == 'vgg16':
        return dict_hash_vgg16_vggface2
    else:
        raise ValueError(f'No hash dict for {dataset} and {model_arch}')